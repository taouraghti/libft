#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*t;

	i = 0;
	while (s1[i])
		i++;
	if (!(t = malloc(sizeof(char) * (i + 1))))
		return (0);
	i = 0;
	while (s1[i])
	{
		t[i] = s1[i];
		i++;
	}
	t[i] = '\0';
	return (t);
}
