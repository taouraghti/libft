#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*a;

	i = 0;
	a = (char *)s;
	while (*a != '\0')
	{
		a++;
		i++;
	}
	while (i != 0)
	{
		if (*a == c)
			return (a);
		a--;
		i--;
	}
	if (*a == c)
		return (a);
	return (0);
}
